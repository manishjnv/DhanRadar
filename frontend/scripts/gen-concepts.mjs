#!/usr/bin/env node
/**
 * gen-concepts.mjs — DhanRadar concept registry pipeline (UI_DATA_ARCHITECTURE_PLAN.md §5/§15).
 *
 * Mirrors scripts/gen-tokens.mjs: ONE source of truth (concepts.json + components.json, read by
 * BOTH the frontend and the backend serialization boundary) → generated, typed artifacts.
 *
 * Reads:  src/data/concepts.json   (Data Concept Registry — §16)
 *         src/data/components.json  (Component Manifest — §17)
 * Emits:  src/data/concepts.generated.ts  — ConceptId union, typed CONCEPTS/COMPONENTS,
 *                                            conceptMeta lookup, conceptKey() query-key helper
 *         src/data/concepts.lock.json      — I4 additive-only baseline (intrinsic axes)
 *
 * Validates: structural + enums, I8 (every manifest concept resolves to a registry concept),
 *            I4 (additive-only — an existing concept may not be removed nor have an intrinsic
 *            axis — visibility/data_class/content_class — changed; access_tier IS overridable §6).
 *
 * `--check` (CI) regenerates in memory and FAILS on: a validation error, an I4 breaking change,
 * or a stale generated file / lock — so the registry can never silently drift.
 */
import { readFileSync, writeFileSync, existsSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __dir = dirname(fileURLToPath(import.meta.url));
const root = join(__dir, '..');
const dataDir = join(root, 'src', 'data');

const CHECK = process.argv.includes('--check');

const CONTENT_CLASS = ['PUBLIC', 'MARKET', 'PERSONAL', 'CALCULATED', 'DERIVED', 'COMPLIANCE', 'AI_GENERATED', 'SYSTEM'];
const VISIBILITY = ['public', 'educational', 'gated'];
const DATA_CLASS = ['public-fact', 'user-personal', 'derived-personal'];
const TIER = ['free', 'plus'];
const STATUS = ['live', 'build', 'data-starved', 'gated-never'];
const INTRINSIC = ['visibility_class', 'data_class', 'content_class']; // I4 — not overridable

const errors = [];
const fail = (m) => errors.push(m);

const concepts = JSON.parse(readFileSync(join(dataDir, 'concepts.json'), 'utf8')).concepts;
const components = JSON.parse(readFileSync(join(dataDir, 'components.json'), 'utf8')).components;

// ---- structural + enum validation --------------------------------------------------------
const ids = new Set();
for (const c of concepts) {
  if (!c.concept) fail(`concept row missing 'concept' id`);
  if (ids.has(c.concept)) fail(`duplicate concept id: ${c.concept}`);
  ids.add(c.concept);
  if (!CONTENT_CLASS.includes(c.content_class)) fail(`${c.concept}: bad content_class '${c.content_class}'`);
  if (!VISIBILITY.includes(c.visibility_class)) fail(`${c.concept}: bad visibility_class '${c.visibility_class}'`);
  if (!DATA_CLASS.includes(c.data_class)) fail(`${c.concept}: bad data_class '${c.data_class}'`);
  if (!TIER.includes(c.access_tier)) fail(`${c.concept}: bad access_tier '${c.access_tier}'`);
  if (!STATUS.includes(c.status)) fail(`${c.concept}: bad status '${c.status}'`);
  // a gated concept must carry the flag the boundary checks (§31); a non-gated one must not.
  if (c.visibility_class === 'gated' && !c.gate_flag) fail(`${c.concept}: gated concept needs a gate_flag`);
  if (c.visibility_class !== 'gated' && c.gate_flag) fail(`${c.concept}: gate_flag only on gated concepts`);
}

// ---- I8: every manifest concept resolves to a registry concept ---------------------------
for (const m of components) {
  for (const id of m.concepts ?? []) {
    if (!ids.has(id)) fail(`I8: component '${m.component}' references unknown concept '${id}'`);
  }
}

// ---- I4: additive-only vs the committed lock (intrinsic axes only) ------------------------
const lockPath = join(dataDir, 'concepts.lock.json');
const desiredLock = {};
for (const c of concepts) {
  desiredLock[c.concept] = { visibility_class: c.visibility_class, data_class: c.data_class, content_class: c.content_class };
}
const prevLock = existsSync(lockPath) ? JSON.parse(readFileSync(lockPath, 'utf8')) : {};
for (const [id, locked] of Object.entries(prevLock)) {
  if (!desiredLock[id]) {
    fail(`I4: concept '${id}' was removed — the contract is additive-only (deprecate, don't delete)`);
    continue;
  }
  for (const axis of INTRINSIC) {
    if (desiredLock[id][axis] !== locked[axis]) {
      fail(`I4: concept '${id}' changed intrinsic '${axis}' (${locked[axis]} → ${desiredLock[id][axis]}) — not allowed`);
    }
  }
}

if (errors.length) {
  console.error('✗  concept registry invalid:\n   - ' + errors.join('\n   - '));
  process.exit(1);
}

// ---- emit the typed view -----------------------------------------------------------------
const union = concepts.map((c) => `  | '${c.concept}'`).join('\n');
const generated = `/* DhanRadar — generated by scripts/gen-concepts.mjs — DO NOT EDIT */
/* Source of truth: src/data/concepts.json + src/data/components.json                */
import conceptsData from './concepts.json';
import componentsData from './components.json';
import type { ComponentDef, ConceptDef } from './registry.types';

/** Every concept id in the registry (literal union, for type-safe consumers). */
export type ConceptId =
${union};

export const CONCEPTS = conceptsData.concepts as readonly ConceptDef[];
export const COMPONENTS = componentsData.components as readonly ComponentDef[];

/** O(1) lookup of a concept's registry row by id. */
export const conceptMeta: Readonly<Record<ConceptId, ConceptDef>> = Object.freeze(
  Object.fromEntries(CONCEPTS.map((c) => [c.concept, c])) as Record<ConceptId, ConceptDef>,
);

/**
 * Single TanStack query-key factory for any concept (§5). One shape, never duplicated —
 * coexists with the hand-maintained keys in lib/queryKeys.ts.
 *   conceptKey('holdings.list', portfolioId)
 */
export const conceptKey = (concept: ConceptId, ...scope: (string | number)[]) =>
  ['concept', concept, ...scope] as const;
`;

const genPath = join(dataDir, 'concepts.generated.ts');
const lockStr = JSON.stringify(desiredLock, null, 2) + '\n';

// Backend serialization-boundary registry (A3, §10 layer 8): the SAME source of truth, emitted as a
// committed JSON the backend image carries (its Dockerfile copies dhanradar/ only, not frontend/), so
// the backend boundary derives visibility/data_class/tier/content_class from concepts.json — never a
// hand-copied list. `npm run check:concepts` (CI) fails if this drifts.
const backendReg = {};
for (const c of concepts) {
  backendReg[c.concept] = {
    visibility_class: c.visibility_class,
    data_class: c.data_class,
    access_tier: c.access_tier,
    content_class: c.content_class,
    gate_flag: c.gate_flag ?? null,
    status: c.status,
  };
}
const backendPath = join(root, '..', 'backend', 'dhanradar', 'mf', 'concepts_registry.json');
const backendStr = JSON.stringify(
  { _generated: 'gen-concepts.mjs from frontend/src/data/concepts.json — DO NOT EDIT', concepts: backendReg },
  null,
  2,
) + '\n';

if (CHECK) {
  let stale = false;
  const curGen = existsSync(genPath) ? readFileSync(genPath, 'utf8') : '';
  if (curGen !== generated) {
    stale = true;
    console.error('✗  STALE: src/data/concepts.generated.ts — run `npm run gen:concepts`');
  }
  const curLock = existsSync(lockPath) ? readFileSync(lockPath, 'utf8') : '';
  if (curLock !== lockStr) {
    stale = true;
    console.error('✗  STALE: src/data/concepts.lock.json — run `npm run gen:concepts` and commit it');
  }
  const curBackend = existsSync(backendPath) ? readFileSync(backendPath, 'utf8') : '';
  if (curBackend !== backendStr) {
    stale = true;
    console.error('✗  STALE: backend/dhanradar/mf/concepts_registry.json — run `npm run gen:concepts`');
  }
  if (stale) process.exit(1);
  console.log(`✓  concept registry in sync (${concepts.length} concepts, ${components.length} components).`);
} else {
  writeFileSync(genPath, generated, 'utf8');
  writeFileSync(lockPath, lockStr, 'utf8');
  writeFileSync(backendPath, backendStr, 'utf8');
  console.log(`✓  src/data/concepts.generated.ts (${concepts.length} concepts)`);
  console.log(`✓  src/data/concepts.lock.json`);
  console.log(`✓  backend/dhanradar/mf/concepts_registry.json`);
}
